#include "../ft_printf.h"

int		ft_isconv(char c)
{
	if (c == 'c' || c == 's' || c == 'd' || c == 'i')
		return (1);
	else if (c == 'p' || c == 'x' || c == 'X' || c == 'u' || c == '%')
		return (1);
	return (0);
}
